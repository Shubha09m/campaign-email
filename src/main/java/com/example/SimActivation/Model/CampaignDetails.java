package com.example.SimActivation.Model;

public class CampaignDetails {
	
	private String title;
	private String template;
	private String createdDate;
	private String company;
	private String perference_target_ids;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTemplate() {
		return template;
	}
	public void setTemplate(String template) {
		this.template = template;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getPerference_target_ids() {
		return perference_target_ids;
	}
	public void setPerference_target_ids(String perference_target_ids) {
		this.perference_target_ids = perference_target_ids;
	}
	
	
}
